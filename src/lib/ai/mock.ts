function prismaSchema(req: GenerateRequest, entities: SchemaEntity[]): string {
  const provider =
    req.database === "mysql" ? "mysql" : req.database === "sqlite" ? "sqlite" : "postgresql";
  const models = entities
    .map((e) => {
      const fields = e.fields
        .filter((f) => !["id", "createdAt", "updatedAt"].includes(f.name))
        .map((f) => `  ${f.name} ${prismaType(f.type)}`)
        .join("\n");
      return `model ${e.name} {
  id        String   @id @default(cuid())
${fields || "  name      String"}
  createdAt DateTime @default(now())
  updatedAt DateTime @updatedAt
}`;
    })
    .join("\n\n");
  return `generator client {
  provider = "prisma-client-js"
}

datasource db {
  provider = "${provider}"
  url      = env("DATABASE_URL")
}

model User {
  id        String   @id @default(cuid())
  name      String
  email     String   @unique
  password  String
  createdAt DateTime @default(now())
  updatedAt DateTime @updatedAt
}

${models}
`;
}

function prismaType(t: string): string {
  const x = t.toLowerCase();
  if (x.includes("number") || x.includes("int")) return "Int?";
  if (x.includes("float") || x.includes("decimal")) return "Float?";
  if (x.includes("bool")) return "Boolean?";
  if (x.includes("date")) return "DateTime?";
  return "String?";
}

