  const analysis: SchemaAnalysis = analyzeSchema(req.input, req.schemaType);
  // Guarantee a User entity exists for the auth module.
  let entities = analysis.entities.filter((e) => e.name.toLowerCase() !== "user");
  if (entities.length === 0) {
    entities = [
      {
        name: "Item",
        fields: [
          { name: "name", type: "String" },
          { name: "description", type: "String" },
          { name: "price", type: "Number" },
        ],
        primaryKey: "id",
        relations: [],
      },
    ];
  }
  entities = entities.slice(0, 8); // keep the demo output focused

  const mongo = isMongo(req);
  const endpoints = buildEndpoints(req, entities);
  const files: GeneratedFile[] = [];

  const add = (path: string, code: string) => files.push({ path, code });

  // Root
  add("package.json", packageJson(req, entities));
  add("tsconfig.json", tsconfig());
  add(".env.example", envExample(req));
  add("README.md", readme(req, entities));

  // Config
  add("src/config/env.ts", envConfig());
  add("src/config/database.ts", databaseConfig(req));

  // App
  add("src/server.ts", serverTs());
  add("src/app.ts", appTs(req, entities));

  // Utils
  add("src/utils/logger.ts", loggerUtil());
  add("src/utils/api-error.ts", apiErrorUtil());
  add("src/utils/pagination.ts", paginationUtil());
  add("src/utils/seed.ts", seedScript(req, entities));

  // Middleware
  add("src/middleware/error.middleware.ts", errorMiddleware());
  add("src/middleware/notFound.middleware.ts", notFoundMiddleware());
  add("src/middleware/validate.middleware.ts", validateMiddleware());
  if (req.auth.jwt) add("src/middleware/auth.middleware.ts", authMiddleware());

  // Auth module
  add("src/services/auth.service.ts", authService(req));
  add("src/controllers/auth.controller.ts", authController(req));
