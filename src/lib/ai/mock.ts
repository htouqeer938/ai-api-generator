function authService(req: GenerateRequest): string {
  const mongo = isMongo(req);
  const store = mongo ? "UserModel" : "prisma.user";
  return `import bcrypt from 'bcryptjs';
import jwt from 'jsonwebtoken';
import { env } from '../config/env';
import { ApiError } from '../utils/api-error';
${mongo ? "import { UserModel } from '../models/user.model';" : "import { prisma } from '../config/database';"}

function signAccessToken(user: { id: string; email: string }) {
  return jwt.sign({ id: user.id, email: user.email }, env.JWT_SECRET, { expiresIn: env.JWT_EXPIRES_IN });
}
${req.auth.refreshToken ? `
function signRefreshToken(user: { id: string; email: string }) {
  return jwt.sign({ id: user.id, email: user.email }, env.JWT_REFRESH_SECRET ?? env.JWT_SECRET, { expiresIn: env.JWT_REFRESH_EXPIRES_IN });
}` : ""}

export const AuthService = {
  async register(input: { name: string; email: string; password: string }) {
    const existing = await ${mongo ? `${store}.findOne({ email: input.email })` : `${store}.findUnique({ where: { email: input.email } })`};
    if (existing) throw ApiError.conflict('Email already registered');
    const passwordHash = await bcrypt.hash(input.password, 10);
    const user = await ${mongo
      ? `${store}.create({ name: input.name, email: input.email, password: passwordHash })`
      : `${store}.create({ data: { name: input.name, email: input.email, password: passwordHash } })`};
    const id = String(${mongo ? "user._id" : "user.id"});
    return this.buildAuthResponse({ id, email: user.email, name: user.name });
  },

  async login(input: { email: string; password: string }) {
    const user = await ${mongo ? `${store}.findOne({ email: input.email })` : `${store}.findUnique({ where: { email: input.email } })`};
    if (!user) throw ApiError.unauthorized('Invalid credentials');
    const ok = await bcrypt.compare(input.password, user.password);
    if (!ok) throw ApiError.unauthorized('Invalid credentials');
    const id = String(${mongo ? "user._id" : "user.id"});
    return this.buildAuthResponse({ id, email: user.email, name: user.name });
  },
${req.auth.refreshToken ? `
  async refresh(token: string) {
    try {
      const payload = jwt.verify(token, env.JWT_REFRESH_SECRET ?? env.JWT_SECRET) as { id: string; email: string };
      return { accessToken: signAccessToken(payload), refreshToken: signRefreshToken(payload) };
    } catch {
      throw ApiError.unauthorized('Invalid refresh token');
    }
  },
` : ""}
  buildAuthResponse(user: { id: string; email: string; name: string }) {
    return {
      user: { id: user.id, email: user.email, name: user.name },
      accessToken: signAccessToken(user),${req.auth.refreshToken ? "\n      refreshToken: signRefreshToken(user)," : ""}
    };
  },
};
`;
}

