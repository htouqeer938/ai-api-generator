function paginationUtil(): string {
  return `import { Request } from 'express';

export interface PageParams {
  page: number;
  limit: number;
  skip: number;
  sort: string;
  order: 'asc' | 'desc';
  search: string;
}

export function getPageParams(req: Request): PageParams {
  const page = Math.max(1, parseInt(String(req.query.page ?? '1'), 10) || 1);
  const limit = Math.min(100, Math.max(1, parseInt(String(req.query.limit ?? '20'), 10) || 20));
  const sort = String(req.query.sort ?? 'createdAt');
  const order = String(req.query.order ?? 'desc') === 'asc' ? 'asc' : 'desc';
  const search = String(req.query.search ?? '').trim();
  return { page, limit, skip: (page - 1) * limit, sort, order, search };
}

export function buildMeta(total: number, p: PageParams) {
  return {
    total,
    page: p.page,
    limit: p.limit,
    totalPages: Math.ceil(total / p.limit) || 1,
  };
}
`;
}

