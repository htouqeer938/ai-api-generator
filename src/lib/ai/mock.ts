function mongoType(t: string): string {
  const x = t.toLowerCase();
  if (x.includes("number") || x.includes("int") || x.includes("float") || x.includes("decimal")) return "Number";
  if (x.includes("bool")) return "Boolean";
  if (x.includes("date")) return "Date";
  if (x.includes("objectid")) return "Schema.Types.ObjectId";
  return "String";
}

function tsType(t: string): string {
  const x = t.toLowerCase();
  if (x.includes("number") || x.includes("int") || x.includes("float") || x.includes("decimal")) return "number";
  if (x.includes("bool")) return "boolean";
  if (x.includes("date")) return "Date";
  return "string";
}

function entityService(req: GenerateRequest, e: SchemaEntity): string {
  const mongo = isMongo(req);
  const model = mongo ? `${e.name}Model` : `${lower(e.name)}Repository`;
  const imp = mongo
    ? `import { ${e.name}Model } from '../models/${lower(e.name)}.model';`
    : `import { ${lower(e.name)}Repository } from '../models/${lower(e.name)}.model';`;
  const searchField = e.fields.find((f) => tsType(f.type) === "string")?.name ?? "name";

  return `import { PageParams, buildMeta } from '../utils/pagination';
import { ApiError } from '../utils/api-error';
${imp}

export const ${e.name}Service = {
  async list(p: PageParams) {
${mongo
    ? `    const filter = p.search ? { ${searchField}: { $regex: p.search, $options: 'i' } } : {};
    const [items, total] = await Promise.all([
      ${model}.find(filter).sort({ [p.sort]: p.order === 'asc' ? 1 : -1 }).skip(p.skip).limit(p.limit),
      ${model}.countDocuments(filter),
    ]);`
    : `    const where = p.search ? { ${searchField}: { contains: p.search, mode: 'insensitive' as const } } : {};
    const [items, total] = await Promise.all([
      ${model}.findMany({ where, orderBy: { [p.sort]: p.order }, skip: p.skip, take: p.limit }),
      ${model}.count({ where }),
    ]);`}
    return { items, meta: buildMeta(total, p) };
  },

  async getById(id: string) {
    const item = await ${mongo ? `${model}.findById(id)` : `${model}.findUnique({ where: { id } })`};
    if (!item) throw ApiError.notFound('${e.name} not found');
    return item;
  },

  async create(data: Record<string, unknown>) {
    return ${mongo ? `${model}.create(data)` : `${model}.create({ data })`};
  },

  async update(id: string, data: Record<string, unknown>) {
    await this.getById(id);
    return ${mongo ? `${model}.findByIdAndUpdate(id, data, { new: true })` : `${model}.update({ where: { id }, data })`};
  },

  async remove(id: string) {
    await this.getById(id);
    return ${mongo ? `${model}.findByIdAndDelete(id)` : `${model}.delete({ where: { id } })`};
  },
};
`;
}

