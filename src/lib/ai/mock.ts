const lower = (s: string) => s.charAt(0).toLowerCase() + s.slice(1);
const plural = (s: string) =>
  s.endsWith("y") ? s.slice(0, -1) + "ies" : s.endsWith("s") ? s + "es" : s + "s";
const kebabPlural = (s: string) => plural(lower(s)).replace(/([A-Z])/g, "-$1").toLowerCase();

